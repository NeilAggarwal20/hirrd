import { useParams, useLocation, Link } from "react-router-dom";
import { useQuery } from "@tanstack/react-query";
import { Sparkles, History, RotateCcw, Search, LayoutDashboard } from "lucide-react";
import { useCurrentUser } from "@/hooks/use-current-user";
import { fetchMockInterviewById, type MockInterviewHistoryItem } from "@/api/mock-interview";
import { ROUTES } from "@/constants/routes";
import { Button } from "@/components/ui/button";
import { InterviewResultsView } from "@/components/shared/interview-results-view";
import { ListSkeleton } from "@/components/shared/list-skeleton";
import { formatDate } from "@/utils/format";

export function InterviewResultsPage() {
  const { id } = useParams<{ id: string }>();
  const location = useLocation();
  const { profile } = useCurrentUser();
  const candidateId = profile?.id ?? "";

  // Arriving right after finishing an interview (or from a history-list
  // click) already carries the full record — no need to re-fetch. A
  // direct visit (refresh, bookmark, shared link) has no state, so falls
  // through to fetching by id instead.
  const preloaded = (location.state as { interview?: MockInterviewHistoryItem } | null)?.interview ?? null;

  const query = useQuery({
    queryKey: ["mock-interview-result", id],
    queryFn: () => fetchMockInterviewById(id!, candidateId),
    enabled: !preloaded && !!id && !!candidateId,
  });

  const interview = preloaded ?? query.data;
  const isLoading = !preloaded && query.isLoading;
  const notFound = !preloaded && !query.isLoading && !query.data;

  return (
    <div>
      <p className="font-mono text-xs uppercase tracking-[0.2em] text-signal">Candidate</p>
      <h1 className="mt-2 font-display text-3xl font-extrabold uppercase tracking-tight text-ink">
        Interview Results
      </h1>

      {isLoading && <ListSkeleton />}

      {notFound && (
        <div className="mt-8 border border-grid p-6 text-center">
          <p className="text-sm text-ink-soft">
            We couldn't find that interview — it may have been for a role that's since been removed.
          </p>
          <Button asChild size="sm" variant="outline" className="mt-4">
            <Link to={ROUTES.candidateInterviews}>View interview history</Link>
          </Button>
        </div>
      )}

      {interview && (
        <>
          <div className="mt-6 border-b border-grid pb-6">
            <p className="flex items-center gap-2 font-display text-lg font-bold text-ink">
              <Sparkles className="h-4 w-4 text-signal" aria-hidden="true" />
              {interview.jobTitle}
            </p>
            <p className="mt-1.5 flex flex-wrap items-center gap-x-2 font-mono text-xs uppercase tracking-wide text-ink-soft">
              <span>{interview.companyName}</span>
              <span>·</span>
              <span>Completed {formatDate(interview.createdAt)}</span>
              <span>·</span>
              <span>Generated by Gemini</span>
            </p>
          </div>

          <div className="mt-8">
            <InterviewResultsView result={interview} />
          </div>

          <div className="mt-10 flex flex-wrap gap-3 border-t border-grid pt-8">
            <Button asChild size="sm">
              <Link to={ROUTES.candidateInterviews}>
                <History className="h-3.5 w-3.5" /> View interview history
              </Link>
            </Button>
            <Button asChild size="sm" variant="outline">
              <Link to={ROUTES.jobDetail(interview.jobId)}>
                <RotateCcw className="h-3.5 w-3.5" /> Start another mock interview
              </Link>
            </Button>
            <Button asChild size="sm" variant="outline">
              <Link to={ROUTES.jobs}>
                <Search className="h-3.5 w-3.5" /> Browse jobs
              </Link>
            </Button>
            <Button asChild size="sm" variant="outline">
              <Link to={ROUTES.candidateDashboard}>
                <LayoutDashboard className="h-3.5 w-3.5" /> Back to dashboard
              </Link>
            </Button>
          </div>
        </>
      )}
    </div>
  );
}