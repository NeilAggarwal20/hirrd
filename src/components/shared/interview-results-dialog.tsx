import * as Dialog from "@radix-ui/react-dialog";
import { motion } from "framer-motion";
import { Sparkles, X, CheckCircle2, AlertTriangle, Lightbulb } from "lucide-react";
import { Button } from "@/components/ui/button";
import type { MockInterviewFeedback } from "@/api/mock-interview";

interface InterviewResultsDialogProps {
  isOpen: boolean;
  onOpenChange: (open: boolean) => void;
  jobTitle?: string;
  result: MockInterviewFeedback | null;
}

function ScoreCard({ label, score, hero }: { label: string; score: number; hero?: boolean }) {
  return (
    <div className={`border border-grid p-4 ${hero ? "bg-paper-dim" : ""}`}>
      <p className="font-mono text-xs uppercase tracking-[0.2em] text-ink-soft">{label}</p>
      <p className={`mt-2 font-display font-extrabold tabular-nums text-ink ${hero ? "text-5xl" : "text-3xl"}`}>
        {score}
        <span className={`font-sans font-normal text-ink-soft ${hero ? "text-xl" : "text-base"}`}>%</span>
      </p>
      <div className="mt-3 h-1.5 w-full bg-paper">
        <div className={`h-1.5 ${hero ? "bg-signal" : "bg-ink"}`} style={{ width: `${score}%` }} />
      </div>
    </div>
  );
}

function Section({ title, icon, children }: { title: string; icon?: React.ReactNode; children: React.ReactNode }) {
  return (
    <div>
      <p className="mb-2 flex items-center gap-1.5 font-mono text-xs uppercase tracking-[0.2em] text-ink-soft">
        {icon}
        {title}
      </p>
      {children}
    </div>
  );
}

const fadeInUp = { initial: { opacity: 0, y: 10 }, animate: { opacity: 1, y: 0 } };

export function InterviewResultsDialog({ isOpen, onOpenChange, jobTitle, result }: InterviewResultsDialogProps) {
  return (
    <Dialog.Root open={isOpen} onOpenChange={onOpenChange}>
      <Dialog.Portal>
        <Dialog.Overlay className="fixed inset-0 bg-black/55 backdrop-blur-xs z-50 transition-all duration-200" />
        <Dialog.Content className="fixed top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 w-[92vw] max-w-2xl max-h-[85vh] overflow-y-auto border border-grid bg-paper p-6 shadow-xl z-50 focus:outline-none animate-in fade-in zoom-in-95 duration-200">
          <div className="flex items-start justify-between gap-4 border-b border-grid pb-4 mb-6">
            <div>
              <Dialog.Title className="flex items-center gap-2 font-display text-lg font-bold uppercase tracking-tight text-ink">
                <Sparkles className="h-4 w-4 text-signal" aria-hidden="true" />
                Interview Feedback
              </Dialog.Title>
              {jobTitle && <p className="mt-1 text-sm font-medium text-ink">{jobTitle}</p>}
              <Dialog.Description className="mt-1 font-mono text-xs uppercase tracking-wide text-ink-soft">
                Generated by Gemini
              </Dialog.Description>
            </div>
            <Dialog.Close asChild>
              <Button
                variant="outline"
                size="sm"
                className="h-8 w-8 shrink-0 p-0 border-grid hover:border-signal hover:text-signal text-ink-soft cursor-pointer"
                aria-label="Close dialog"
              >
                <X className="h-4 w-4" />
              </Button>
            </Dialog.Close>
          </div>

          {result && (
            <div className="space-y-6">
              <motion.div {...fadeInUp} transition={{ duration: 0.3 }} className="grid grid-cols-3 gap-3">
                <ScoreCard label="Overall" score={result.overallScore} hero />
                <ScoreCard label="Technical" score={result.technicalScore} />
                <ScoreCard label="Communication" score={result.communicationScore} />
              </motion.div>

              {result.strengths.length > 0 && (
                <motion.div {...fadeInUp} transition={{ duration: 0.3, delay: 0.06 }}>
                  <Section title="Strengths" icon={<CheckCircle2 className="h-3.5 w-3.5 text-meadow" />}>
                    <ul className="space-y-1.5">
                      {result.strengths.map((item, i) => (
                        <li key={i} className="flex gap-2 text-sm text-ink">
                          <CheckCircle2 className="mt-0.5 h-3.5 w-3.5 shrink-0 text-meadow" aria-hidden="true" />
                          <span>{item}</span>
                        </li>
                      ))}
                    </ul>
                  </Section>
                </motion.div>
              )}

              {(result.weaknesses.length > 0 || result.improvements.length > 0) && (
                <motion.div {...fadeInUp} transition={{ duration: 0.3, delay: 0.12 }}>
                  <Section title="Areas to improve" icon={<AlertTriangle className="h-3.5 w-3.5 text-amber" />}>
                    <ul className="space-y-1.5">
                      {[...result.weaknesses, ...result.improvements].map((item, i) => (
                        <li key={i} className="flex gap-2 text-sm text-ink">
                          <AlertTriangle className="mt-0.5 h-3.5 w-3.5 shrink-0 text-amber" aria-hidden="true" />
                          <span>{item}</span>
                        </li>
                      ))}
                    </ul>
                  </Section>
                </motion.div>
              )}

              {result.sampleBetterAnswers.length > 0 && (
                <motion.div {...fadeInUp} transition={{ duration: 0.3, delay: 0.18 }}>
                  <Section title="Suggested better answers" icon={<Lightbulb className="h-3.5 w-3.5 text-signal" />}>
                    <ul className="space-y-2.5">
                      {result.sampleBetterAnswers.map((item, i) => (
                        <li key={i} className="border border-grid bg-paper-dim p-3 text-sm text-ink">
                          {item}
                        </li>
                      ))}
                    </ul>
                  </Section>
                </motion.div>
              )}
            </div>
          )}
        </Dialog.Content>
      </Dialog.Portal>
    </Dialog.Root>
  );
}